void main() {
  // Variables and Data Types
  String name = "Mahia";
  int age = 22;
  double cgpa = 3.84;
  bool isStudent = true;

  print("Name: $name");
  print("Age: $age");
  print("CGPA: $cgpa");
  print("Student: $isStudent");

  // var keyword
  var city = "Dhaka";
  var zipCode = 1704;
  var temperature = 34.5;

  print("City: $city");
  print("Zip Code: $zipCode");
  print("Temperature: $temperature");

  // final keyword
  final universityName = "Independent University, Bangladesh";

  print("University: $universityName");
}